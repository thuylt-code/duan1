    private boolean check() {
        if (txtMaKH.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Mã Nhân Viên!");
            return false;
        } else if (txtTenKH.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Tên nhân viên !");
            return false;
        } else if (txtDiaChi.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Địa chỉ !");
            return false;
        } else if (txtEmail.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Email !");
            return false;
        } else if (!vali.checkDate(txtNgaySinh.getDate())) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Ngày sinh !");
            return false;
        } else if (!vali.checkEmail(txtEmail.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại Email !");
            return false;
        }else if (!vali.checkSdt(txtSdt.getText().trim()) ){
            JOptionPane.showMessageDialog(this,"Kiểm tra lại số điện thoại !");
            return false;
        }
        return true;
    }
     

