    public void fillToTable(){
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setNumRows(0);
        int index=1;
        for (KhachHang khachHang : list) {
            Object[] row= {index++,khachHang.getMaKH(), khachHang.getTenKH(), khachHang.getGioiTinh()?"Nam":"Nữ", khachHang.getSdt(),khachHang.getEmail(), khachHang.getDiaChi(), khachHang.getNgaySinh(),khachHang.getTrangThai() == 1 ? "Còn hoạt động" : "Ngưng hoạt động"};
            model.addRow(row);
        }
    }
    
    public void control(int index){
        KhachHang kh = list.get(index);
        txtMaKH.setText(kh.getMaKH());
        txtTenKH.setText(kh.getTenKH());
        txtSdt.setText(kh.getSdt());
        boolean gt=true;
        if(kh.getGioiTinh()) {
            rdonam.setSelected(gt=true);
            rdonu.setSelected(gt=false);
        }else{
            rdonu.setSelected(gt=true);
            rdonam.setSelected(gt=false);
        }
        
        if(rdonu.isSelected()) {
        }
        txtDiaChi.setText(kh.getDiaChi());
        txtEmail.setText(kh.getEmail());
        txtNgaySinh.setDate(new Date(kh.getNgaySinh().getTime()) );
        if(kh.getTrangThai()==1){
            rdoon.setSelected(true);
            rdooff.setSelected(false);
        }else {
            rdooff.setSelected(true);
            rdoon.setSelected(false);
        }
    }
    
